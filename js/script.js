// Function to toggle course information display
function toggleInfo(card) {
  const info = card.querySelector('.course-info');
  info.style.display = (info.style.display === 'none' || info.style.display === '') ? 'block' : 'none';

  // Close burger menu if open
  closeBurgerMenu();
}

// Function to toggle mobile navigation menu
document.querySelector('.burger-menu').addEventListener('click', function () {
  const nav = document.querySelector('.navbar');
  nav.style.display = (nav.style.display === 'none' || nav.style.display === '') ? 'flex' : 'none';

  const footerNav = document.querySelector('.footer-navbar');
  footerNav.style.display = (footerNav.style.display === 'none' || footerNav.style.display === '') ? 'flex' : 'none';
});

// Function to close burger menu
function closeBurgerMenu() {
  const nav = document.querySelector('.navbar');
  const footerNav = document.querySelector('.footer-navbar');

  // Check if the navigation menu is open, then close it
  if (nav.style.display === 'flex' || footerNav.style.display === 'flex') {
    nav.style.display = 'none';
    footerNav.style.display = 'none';
  }
}

// Close burger menu when clicking outside of it
document.addEventListener('click', function (event) {
  const isBurgerMenu = event.target.classList.contains('burger-menu');
  const isNavbar = event.target.closest('.navbar');
  const isFooterNavbar = event.target.closest('.footer-navbar');

  if (!isBurgerMenu && !isNavbar && !isFooterNavbar) {
    closeBurgerMenu();
  }
});




